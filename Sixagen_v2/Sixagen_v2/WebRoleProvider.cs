        public override string[] GetRolesForUser(string username)
        {
            using (var db = new Sixagenv2Entities())
            {
                var res = from u in db.Login
                          join r in db.Roles on u.ID equals r.IDUsuario
                          where u.Usuario == username
                          select r.Role;
                return res.ToArray();
            }
        }

